# Operações Aritméticas

numero1 = 10
numero2 = 20

print(numero1 + numero2) #Soma
print(numero1 - numero2) #Subtração
print(numero1 * numero2) #Multiplicação
print(numero1 / numero2) #Divisão
print(numero1 // numero2) #Divisão inteira
print(20 % 3) # Módulo
print(2 ** 3) # Potênciação

# Operadores Booleanos

a = 5
b = 10
c = 5

print(a > b)    #Maior que
print(a < b)    #Menor que
print(a >= c)   #Maior ou Igual
print(a <= c)   #Menor ou Igual
print('Python' == 'python') #Igualdade
print('abacaxi' != 'banana') #Diferença
